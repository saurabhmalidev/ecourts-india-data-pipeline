-- 1. Total Civil vs Criminal pending per court level
SELECT c.court_level, ca.case_type, COUNT(*) AS total_pending
FROM cases ca JOIN courts c ON ca.court_id = c.court_id
WHERE ca.status = 'Pending'
GROUP BY c.court_level, ca.case_type
ORDER BY c.court_level, ca.case_type;

-- 2. Age-wise pending cases (dashboard bar chart)
SELECT
    CASE
        WHEN CURRENT_DATE - filing_date < 365   THEN '< 1 Year'
        WHEN CURRENT_DATE - filing_date < 1095  THEN '1 to 3 Years'
        WHEN CURRENT_DATE - filing_date < 1825  THEN '3 to 5 Years'
        WHEN CURRENT_DATE - filing_date < 3650  THEN '5 to 10 Years'
        ELSE 'Above 10 Years'
    END AS age_bucket,
    case_type,
    COUNT(*) AS case_count
FROM cases
WHERE status = 'Pending'
GROUP BY age_bucket, case_type
ORDER BY age_bucket;

-- 3. High Court case subtype breakdown
SELECT ca.case_subtype, ca.case_type, COUNT(*) AS total
FROM cases ca JOIN courts c ON ca.court_id = c.court_id
WHERE c.court_level = 'High' AND ca.status = 'Pending'
GROUP BY ca.case_subtype, ca.case_type
ORDER BY total DESC;

-- 4. Supreme Court coram-wise (judge_count grouping)
SELECT c.judge_count, ca.case_type, COUNT(*) AS total
FROM cases ca JOIN courts c ON ca.court_id = c.court_id
WHERE c.court_level = 'Supreme' AND ca.status = 'Pending'
GROUP BY c.judge_count, ca.case_type;
